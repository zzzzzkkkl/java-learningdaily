public class VariableDemo2{
    public static void main(String[] args){
        //1.变量基本用法
        int a=10;
        System.out.println(a);

        //2.变量参与运算
        int c=10;
        int b=20;
        System.out.println(c+b);

        //3.修改变量记录值
        a=30;
        System.out.println(a);
    }
}